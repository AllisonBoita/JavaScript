// Operadores Comparação

// Igualdade Solta (dado)

console.log(3 == 3)

// Igualdade Estrita (tipo do dado)

console.log('2' === 2)
console.log(2 === 2)

// Desigualdade Solta (dado)

console.log(3 != 3)

// Desigualdade Estrita (tipo do dado)

console.log('3' !== 3)

// Maior que

console.log(3 > 2) // Maior que
console.log(3 >= 3) // Maior ou Igual que
console.log(3 < 2) // Menor que
console.log(3 <= 3) // Menor ou Igual que